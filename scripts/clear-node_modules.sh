#!/bin/bash

echo "Removendo instalações do node_modules não listadas no package.json..."
npm prune
# Esse comando limpa o que estiver instalado no node_modules mas não listado no package.json.

echo ""
echo "Removendo dependências duplicadas no node_modules..."
npm dedupe
# Esse comando organiza o node_modules, agrupando dependências iguais em uma hierarquia mais eficiente — ajuda a reduzir espaço e evitar conflitos.